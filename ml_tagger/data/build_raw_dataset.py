from datasets import load_dataset
import json

OUTPUT_FILE = "ml_tagger/data/raw.json"
MAX_SAMPLES = 50_000  # start small


def main():
    dataset = load_dataset("code_search_net", "python", split="train")

    results = []

    for i, item in enumerate(dataset):
        if i >= MAX_SAMPLES:
            break

        code = item.get("func_code_string", "")
        doc = item.get("func_documentation_string", "")
        
        if not code.strip():
            continue

        # Basic filtering (IMPORTANT)
        if len(code) < 20:
            continue
        
        # skip huge files (noise)
        if len(code) > 2000:
            continue

        # skip autogenerated stuff
        if "import *" in code:
            continue

        # skip tests (optional)
        if "test_" in code:
            continue

        results.append({
            "code": code,
            "docstring": doc
        })

        if i % 1000 == 0:
            print(f"Processed {i}")

    with open(OUTPUT_FILE, "w") as f:
        json.dump(results, f, indent=2)

    print(f"Saved {len(results)} samples")


if __name__ == "__main__":
    main()
    